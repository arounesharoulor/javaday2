package floatdiv;

public class demo {
	public static void main(String argv[]) {
		int value1= 9/2;
		float value2= 101/62;
		double value3= 10d/64;
		float value4=5/2.5f;
		System.out.println("value 1=" + value1);
		System.out.println("value 2=" + value2);
		System.out.println("value 3=" + value3);
		System.out.println("value 4=" + value4);
	}
	

}
